
# !converting
# *converting to int
print(int('344')+int('1'))
# *converting to float
print(float('344')+float('1'))
# *converting to float
print(str(344)+str(1))
# *converting a char to int
a='3'
b='2'
print(ord(a)+ord(b))
# *converting int to hexadecimal string
print(hex(56))
# *converting int to octal string
print(oct(99))
# *converting string to tuple
print(tuple('geek'))#('g','e','e','k')
# *converting string to set
print(tuple('geek'))#{'g','e','e','k'}
# *converting string to list
print(tuple('geek'))#['g','e','e','k']
# *converting a tuple of order (key, value) into a dictionary
print(dict((('a',1),('b',2),('c',3))))#{'a': 1, 'b': 2, 'c': 3}
# *converting integer to complex number
print(complex(1,2))#(1+2j)
# *converting ASCII value to characters
print(chr(76))#L
